    public function actionGenerateFollowersForAll()
    {
        //get all users
        $users1= User::getUserNames();
        $users2= User::getUserNames();
        
        foreach($users1 as $user1)
        {
            foreach($users2 as $user2)
            {
                if($user1['id'] != $user2['id'])
                {
                    //user1 follows user2 (if not already followed)
                    $follower = Follower::model()->findByAttributes(array('followerUserId'=>$user1['id'], 'followedUserId'=>$user2['id']));
                    if(is_null($follower) && empty($follower))
                    {
                        $follower1 = new Follower('follow_api');
                        $follower1->followerUserId = $user1['id'];
                        $follower1->followedUserId = $user2['id'];
                        $follower1->save();
                    }

                    //user2 follows user1  (if not already followed)
                    $follower = Follower::model()->findByAttributes(array('followerUserId'=>$user2['id'], 'followedUserId'=>$user1['id']));
                    if(is_null($follower) && empty($follower))
                    {
                        $follower2 = new Follower('follow_api');
                        $follower2->followerUserId = $user2['id'];
                        $follower2->followedUserId = $user1['id'];
                        $follower2->save();
                    }
                }
            }
        }
    }
