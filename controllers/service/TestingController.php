    public function actionPostOnFacebook()
    {
        $apiName = 'testing/postOnFacebook';
        $accessToken=null;
        
        $_JSON = $this->getJsonInput();
        
        try
        {
            if(!isset($_JSON) || empty($_JSON))
                $result = $this->error($apiName, WS_ERR_POST_PARAM_MISSED, 'No input received.');
            else if(!isset($_JSON['accessToken']) || empty($_JSON['accessToken']))
                $result = $this->error($apiName, WS_ERR_POST_PARAM_MISSED, 'Please enter the access token.');
            else
            {
                $accessToken = filter_var($_JSON['accessToken'], FILTER_SANITIZE_STRING);
                $name = 'FoodTalk';
                $link = 'https://www.google.co.in/';
                $image = Yii::getPathOfAlias('webroot.images.post') . '/20150522184110884315.png';
                $message = 'My message';
                $fbPostId = PostOnFacebook($accessToken, $message, $name, $link, $image);
                $result = array(
                    'api' => $apiName,
                    'apiMessage' => 'Message posted successfully.',
                    'status' => 'OK',
                    'facebookPostId' => $fbPostId
                );
            }
        } 
        catch (Exception $e) 
        {
            $result = $this->error($apiName, $e->getCode(), Yii::t('app', $e->getMessage()));
        }
        $this->sendResponse(json_encode($result, JSON_UNESCAPED_UNICODE));
    }
    
